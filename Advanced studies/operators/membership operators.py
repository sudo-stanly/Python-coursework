#!         Operator                 Description                             Example
#*            in                    Returns True if a sequence with         x in y
#*                                  the specified value is present
#*                                  in the onject.
x = ["apple", "banana"]
print("banana" in x) #* returns True because a sequence with the value "banana" is in the list


#*          not in 	                Returns True if a sequence with the     x not in y
#*                                  specified value is not present in
#*                                  the object.
print("pineapple" not in x) #* returns True because a sequence with the value "pineapple" is not in the list.